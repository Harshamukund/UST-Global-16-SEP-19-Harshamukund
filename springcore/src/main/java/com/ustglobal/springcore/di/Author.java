package com.ustglobal.springcore.di;

public class Author {
	private String Name;
	private String penname;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPenname() {
		return penname;
	}
	public void setPenname(String penname) {
		this.penname = penname;
	}
	

}
